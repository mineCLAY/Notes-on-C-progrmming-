//comma(逗号运算符) 自左向右 ， 有加括号、逗号的赋值 , 取最后一个表达式 , 没加括号就取第一个表达式
//表达式 01: int a = , b =5;
//表达式 02: int c = (a++ , b = 2 * a , a * ++b)

#include <stdio.h>

int main(void){
  int a = 2 , b = 5;
  int c = (a++ , b = 2 * a , a * ++b);
  //逗号表达式的求值顺序是自左向右:
  //01. 先计算 a++ 的值是: 2
  //02. 再计算 b = 2 * b 的值是: 6
  //03. 再计算 a * ++b 的值是: 21
  //04. 最后 把 a * ++b 的值 21 赋值给 c
  printf(" a = %d\n b = %d\n c = %d\n" , a , b , c);
  return 0;
}
